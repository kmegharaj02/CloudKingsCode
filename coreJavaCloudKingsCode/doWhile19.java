package coreJavaCloudKingsCode;

public class doWhile19 {
    public static void main(String[] args) {
        int a=21;
        do{
            System.out.println(a);
            a++;
        }while(a<20);

        int b=1;
        do{
            System.out.println(b);
            b++;
        }while(b<=20);
    }
}
