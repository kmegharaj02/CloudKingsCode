package com.ControlStatements;

public class SampleSwitch {
	public static void main(String args[])
	{
		for(int i=0;i<=3;i++)
			switch(i)
			{
			case 0:
				System.out.println("0");
			break;
			case 1:
				System.out.println("1");
				break;
			case 2:
				System.out.println("a");
				break;
				default:
					System.out.println("default");
					
			
	}

}
}