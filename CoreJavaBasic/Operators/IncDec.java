package com.Operators;

public class IncDec {
	public static void main(String args[])
	{
		int a=5;
		int b=10;
		int c;
		int d;
		c=++a;
		d=b++;
		System.out.println(a+" "+b+" "+c+" "+d++);

		{
		int e=--a;
		int f=b--;
		System.out.print(a+" "+b+" "+c+" "+d+" "+e+" "+f);
	
	}
	}

}
